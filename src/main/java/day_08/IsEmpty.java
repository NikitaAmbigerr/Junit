package day_08;

public class IsEmpty {
    public static void main(String[] args) {
        String str1 = "";
        String str2 = "Hello, World!";

        System.out.println("Is str1 empty? " + str1.isEmpty()); // Output: true
        System.out.println("Is str2 empty? " + str2.isEmpty()); // Output: false
    }
}

