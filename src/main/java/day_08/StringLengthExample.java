package day_08;

public class StringLengthExample {
    public static void main(String[] args) {
        String str1 = "Hello";
        String str2 = "";
        String str3 = "   Leading and trailing spaces   ";

        System.out.println("Length of str1: " + "Hello".length()); // Output: 5
        System.out.println("Length of str2: " + str2.length()); // Output: 0
        System.out.println("Length of str3: " + str3.length()); // Output: 33
    }
}
