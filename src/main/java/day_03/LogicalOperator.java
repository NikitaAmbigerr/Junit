package day_03;

public class LogicalOperator {
	public static void main(String args[]) {
//	Logical Operators Return boolean value &&,||
	 int a=10;
	 int b=20;
	boolean b1=a>b;
	boolean b2=a<b;
	
	System.out.println(b1&&b2); //both true then result is true
	System.out.println(b1||b2);//anyone true then true 
}
}