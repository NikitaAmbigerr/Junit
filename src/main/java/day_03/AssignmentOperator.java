package day_03;

public class AssignmentOperator {

	public static void main(String[] args) {
// Assignment operator =,+=,-=,*=,/=,%=,==
	int a=5;
	System.out.println("a="+a);
	a+=5;//a+5
	System.out.println("a+5="+a);
	a-=5;
	System.out.println("a-5="+a);
	a*=4;
	System.out.println("a*4="+a);
	a/=5;
	System.out.println("a/2="+a);
	a%=2;
	System.out.println("a%2="+a);
	
	}

}
