package day_03;

public class RelationalOperator {

	public static void main(String[] args) {
		
//		3) Relational Operator return boolean value
		
		int a=45;
		int b=6;
		
		System.out.println("a==b="+(a==b));// Equals
		System.out.println("a>b="+(a>b));// greater
		System.out.println("a<b="+(a<b));// lesser
		System.out.println("a>=b="+(a>=b));//greater equal
		System.out.println("a<=b="+(a<=b));// lesser equal
		System.out.println("a!=b="+(a!=b));// Not equal
	}

}
