package day_04;

public class ElseIfCondition {

	public static void main(String[] args) {
		
//	     Check if number is positive or negative
			int num=-8;
			
			if (num>0) {
				System.out.println("Num is positive");	
			}
			else if(num<0) {
				System.out.println("Num is Negative");
			}
			else {
				System.out.println("Num is 0");
			}

	}

}
